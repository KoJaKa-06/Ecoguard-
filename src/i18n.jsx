const LangContext = createContext(null)

export function LangProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem('ecoguard_lang') || 'en')

  useEffect(() => {
    localStorage.setItem('ecoguard_lang', lang)
    document.documentElement.lang = lang
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr'
  }, [lang])

  function t(key) {
    return translations[lang]?.[key] ?? translations.en[key] ?? key
  }

  function toggleLang() {
    setLang(prev => (prev === 'en' ? 'ar' : 'en'))
  }

  return (
    <LangContext.Provider value={{ lang, t, toggleLang, setLang }}>
      {children}
    </LangContext.Provider>
  )
}

export function useLang() {
  const ctx = useContext(LangContext)
  if (!ctx) throw new Error('useLang must be used within LangProvider')
  return ctx
}

// Helper to translate dynamic backend values (categories, urgency, etc.)
export function tValue(t, prefix, value) {
  if (!value) return value
  return t(`${prefix}.${value}`) || value
}
